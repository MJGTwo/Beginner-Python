from math import pi

def rectArea(height, width):
    return height*width
def rectPara(height,width):
    return 2*(height+width)

def circArea(radius):
    return pi * radius ** 2
def circPara(radius):
    return 2 * radius * pi

def squareArea(side):
    return side ** 2
def squarePara(side):
    return 4 * side
